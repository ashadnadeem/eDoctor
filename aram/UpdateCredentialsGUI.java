    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            long ptr = admin.searchString(file,IDTF.getText().trim(),admin.SearchTA);
            file.seek(ptr);
            admin.read(file);
            if(admin.getUserPsw().trim().equals(oldPswTF.getText().trim())){
                if(newPswTF.getText().equals(newPsw2TF.getText()))
                {   
                    admin.setuserpass(IDTF.getText().trim(), newPswTF.getText().trim());
                    admin.updateCredentials(file);
                    JOptionPane.showMessageDialog(this, "New credentials updated successfully");
                    this.setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Password and Confirm Password did not matched. Try Again.");
                }
            } else throw new Blank("Invalid Login");
            
        } catch (FileNotFoundException ex) {
            //Logger.getLogger(UpdateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            //Logger.getLogger(UpdateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Blank ex) {
            JOptionPane.showMessageDialog(null,ex.toString(), "Update Unsuccessfull", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

