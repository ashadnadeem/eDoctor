    private void addJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJBActionPerformed
        // TODO add your handling code here:
        try{
            if(! addPswTF.getText().trim().equals(addPsw2.getText().trim())) throw new Blank("Password and Confirm Password did not matched. Try Again.");
            admin.setValues(NameTF.getText().trim(), ageTF.getText().trim(), addidTF.getText().trim(), addPswTF.getText().trim(), cnicTF.getText().trim(), gen);
            
            admin.write(file);
            JOptionPane.showMessageDialog(null,"Admin Added! " + admin.getName(), "Saved!", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PatientGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PatientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Blank E){
            JOptionPane.showMessageDialog(null,E.toString(), "Error!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addJBActionPerformed

