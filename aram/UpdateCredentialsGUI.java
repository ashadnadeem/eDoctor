    private void removeJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJBActionPerformed
        // TODO add your handling code here:
        try {
            long ptr = admin.searchString(file,removeidTF.getText().trim(),admin.SearchTA);
            file.seek(ptr);
            admin.read(file);
            if(admin.getUserPsw().trim().equals(removePswTF.getText().trim())){
            int remove = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Admin?","Delete Admin?",JOptionPane.YES_NO_OPTION);
            if (remove == 0){
                admin.removeAdmin(file,removeidTF.getText().trim());
                JOptionPane.showMessageDialog(this, "Admin Deleted successfully");
                this.setVisible(false);
            }
            } else throw new Blank("Invalid Login");
            
        } catch (FileNotFoundException ex) {
            //Logger.getLogger(UpdateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            //Logger.getLogger(UpdateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Blank ex) {
            JOptionPane.showMessageDialog(null,ex.toString(), "Update Unsuccessfull", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeJBActionPerformed

